package org.wickedsource.logunit;

/**
 * Log levels supported by {@link LogUnit}. Depending on the used logging
 * framework, the logging frameworks levels have to be translated into LogUnit
 * log levels.
 * 
 * @author Tom
 * 
 */
public enum LogLevel {

	TRACE,

	DEBUG,

	INFO,

	WARN,

	ERROR,

	FATAL;

}
